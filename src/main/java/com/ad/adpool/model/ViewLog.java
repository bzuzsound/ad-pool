package com.ad.adpool.model;

public class ViewLog {
	private int v_no;
	private String v_ip;
	private int v_viewNo;
	public int getV_no() {
		return v_no;
	}
	public void setV_no(int v_no) {
		this.v_no = v_no;
	}
	public String getV_ip() {
		return v_ip;
	}
	public void setV_ip(String v_ip) {
		this.v_ip = v_ip;
	}
	public int getV_viewNo() {
		return v_viewNo;
	}
	public void setV_viewNo(int v_viewNo) {
		this.v_viewNo = v_viewNo;
	}
	
}
